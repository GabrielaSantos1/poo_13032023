
#include <iostream>
using namespace std;

int main() {
    // cria 2 variaveis inteiras no valor 10 e 4
    int num1 = 50, num2 = 5;
    // cria 4 variaveis inteiras
    int soma, multi, divi, modulo, subt;
    
    //soma é igual a num1 mais num2
    soma = num1 + num2;
    //multi é igual a num1 vezes num2
    multi = num1 * num2;
    //divi é igual a num 1 dividido por num2
    divi = num1 / num2;
    //modulo = 
    modulo = num1%num2;
    //subt é igual a num1 menos num 2
    subt = num1 - num2;
    
    //imprime o valor final da variavel para o user
    cout << soma <<endl;
    cout << multi <<endl;
    cout << divi <<endl;
    cout << modulo <<endl;
    cout << subt <<endl;
    
    
    return 0;
    
}
